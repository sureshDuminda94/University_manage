    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        String stu_id_str = student_id.getText();
        String stu_name_str = s_name.getText();
        String stu_nic_str = s_nic.getText();
        String stu_birth_str =s_birth.getText();
        String stu_addr_str = s_address.getText();
        String course = (String) course_selector.getSelectedItem();
         StudentDTO studentDTO = new StudentDTO(0, stu_id_str, stu_name_str, stu_nic_str, stu_birth_str, stu_addr_str,course);
        try {
            boolean result = ManageStudentController.saveStudent(studentDTO);
             if (result) {
                JOptionPane.showMessageDialog(this, "Student has been updated successfully");
                 new Loarding_data().LoadDataInCombobox(course_selector,"subject");
              new Loarding_data().loadAllCourse(student_table,"student","");  
                s_address.setText("");
                s_birth.setText("");
                s_name.setText("");
                s_nic.setText("");;
             }else{}
        
        } catch (Exception ex) {
            Logger.getLogger(Crud_student.class.getName()).log(Level.SEVERE, null, ex);
        }
         
        
    }//GEN-LAST:event_saveBtnActionPerformed

    private void removeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtnActionPerformed
       try {
           
            int id_  =  id;
         boolean result = ManageStudentController.deleteStudent(id_);

            if (result) {
                JOptionPane.showMessageDialog(this, "subject has been remove successfully");
             new Loarding_data().LoadDataInCombobox(course_selector,"subject");
              new Loarding_data().loadAllCourse(student_table,"student","");  
                   s_address.setText("");
                s_birth.setText("");
                s_name.setText("");
                s_nic.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to remove the customer");
            }
        } catch (Exception ex) {
            Logger.getLogger(ManageStudentController.class.getName()).log(Level.SEVERE, null, ex);
        }  

    }//GEN-LAST:event_removeBtnActionPerformed

